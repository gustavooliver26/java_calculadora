package view2;

public class OperacoesMat {

public double soma (double soma1 , double soma2) {
	return soma1+soma2;
}
public double mult (double mult1 , double mult2) {
	return mult1*mult2;
}
public double div (double div1 , double div2) {
	return div1/div2;
}
public double sub (double sub1 , double sub2) {
	return sub1-sub2;
}
public double porcentagem (double a, double b ) {
	return a * (a/100) ;

}

}